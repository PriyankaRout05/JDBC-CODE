
public class ForLoop {
	public static void main(String args[]) {
		int i;
		//print 10 times
		for(i=1;i<=10;i++) {  
		
		System.out.println("priya");
	}
		//print 1 to 100 
		for(i=1;i<=100;i++) {
			System.out.println("20");
		}
		
		//print 65 to 35
		for(i=65;i>=35;i--) {
			System.out.println("i");
		}

}
	}
